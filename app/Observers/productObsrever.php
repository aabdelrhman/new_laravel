    /**
     * Handle the Product "deleted" event.
     *
     * @param  \App\Models\Product  $product
     * @return void
     */
    public function deleting(Product $product)
    {
        if($product -> status == 1){
            $product -> update([
                'status' => 0 ,
            ]);
        }
    }

    /**
     * Handle the Product "restored" event.
     *
     * @param  \App\Models\Product  $product
     * @return void
     */
    public function restored(Product $product)
    {
        if($product -> status == 0){
            $product -> update([
                'status' => 1 ,
            ]);
        }
    }

